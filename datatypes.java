public class datatypes {
    public static void main(String[] args) {

        byte b = 8; // 1 byte = 8 bits
        System.out.println(b);

        char ch = 'a'; // 2 bytes = 16 bits
        System.out.println(ch);

        boolean var = false; // 1 bit
        System.out.println(var);

        float price = 10.5f; // 4 bytes = 32 bits
        System.out.println(price);

        int a = 25; // 4 bytes = 32 bits
        System.out.println(a);

        // double largeDecimal = 1234.5678; // 8 bytes = 64 bits

        // long largeInteger = 123456L; // 8 bytes = 64 bits

        short smallNum = 12345; // 2 bytes = 16 bits
        System.out.println(smallNum);

    }
}
